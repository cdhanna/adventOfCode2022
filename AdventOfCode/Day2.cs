namespace AdventOfCode;

public class Day2
{

    enum Piece
    {
        Rock, Paper, Scissors
    }
    
    enum Outcome
    {
        Win, Loss, Tie
    }
    
    //A for Rock, B for Paper, and C for Scissors
    //X for Rock, Y for Paper, and Z for Scissors
    
    //(1 for Rock, 2 for Paper, and 3 for Scissors)
    //plus  (0 if you lost, 3 if the round was a draw, and 6 if you won).
    [TestCase(sample, 15)]
    [TestCase(input, 13005)]
    public void Part1(string input, int expected)
    {
        var lines = input.Split("\n");

        Piece OppToPiece(char x)
        {
            return x switch
            {
                'A' => Piece.Rock,
                'B' => Piece.Paper,
                'C' => Piece.Scissors
            };
        }
        Piece YouToPiece(char x)
        {
            return x switch
            {
                'X' => Piece.Rock,
                'Y' => Piece.Paper,
                'Z' => Piece.Scissors
            };
        }

        int PieceScore(Piece p) => p switch
        {
            Piece.Rock => 1,
            Piece.Paper => 2,
            Piece.Scissors => 3
        };
        Outcome WinScore(Piece opp, Piece you) => (opp, you) switch
        {
            (Piece.Rock, Piece.Rock) => Outcome.Tie,
            (Piece.Paper, Piece.Rock) => Outcome.Loss,
            (Piece.Scissors, Piece.Rock) => Outcome.Win,
            (Piece.Rock, Piece.Paper) => Outcome.Win,
            (Piece.Paper, Piece.Paper) => Outcome.Tie,
            (Piece.Scissors, Piece.Paper) => Outcome.Loss,
            (Piece.Rock, Piece.Scissors) => Outcome.Loss,
            (Piece.Paper, Piece.Scissors) => Outcome.Win,
            (Piece.Scissors, Piece.Scissors) => Outcome.Tie,
        };

        int OutcomeScore(Outcome o) => o switch
        {
            Outcome.Loss => 0,
            Outcome.Tie => 3,
            Outcome.Win => 6
        };

        var totalScore = 0;
        foreach (var line in lines)
        {
            if (string.IsNullOrEmpty(line)) continue;
            var opp = line[0];
            var self = line[2];

            var oppPiece = OppToPiece(opp);
            var youPiece = YouToPiece(self);

            var outcome = WinScore(oppPiece, youPiece);
            var score = OutcomeScore(outcome) + PieceScore(youPiece);

            totalScore += score;
        }
        
        Assert.AreEqual(expected, totalScore);
        
    }
    
