        }

        int PieceScore(Piece p) => p switch
        {
            Piece.Rock => 1,
            Piece.Paper => 2,
            Piece.Scissors => 3
        };

        Piece GetSelfPiece(Outcome o, Piece opp) => (o, opp) switch
        {
            (Outcome.Loss, Piece.Paper) => Piece.Rock,
            (Outcome.Win, Piece.Paper) => Piece.Scissors,
            (Outcome.Tie, Piece.Paper) => Piece.Paper,
            (Outcome.Loss, Piece.Rock) => Piece.Scissors,
            (Outcome.Win, Piece.Rock) => Piece.Paper,
            (Outcome.Tie, Piece.Rock) => Piece.Rock,
            (Outcome.Loss, Piece.Scissors) => Piece.Paper,
            (Outcome.Win, Piece.Scissors) => Piece.Rock,
            (Outcome.Tie, Piece.Scissors) => Piece.Scissors,

        };

        int OutcomeScore(Outcome o) => o switch
        {
            Outcome.Loss => 0,
            Outcome.Tie => 3,
            Outcome.Win => 6
        };

        var totalScore = 0;
        foreach (var line in lines)
        {
            if (string.IsNullOrEmpty(line)) continue;
            var opp = line[0];
            var self = line[2];

            var oppPiece = OppToPiece(opp);
            var outcome = YouToOutcome(self);
            
            var score = OutcomeScore(outcome) + PieceScore(GetSelfPiece(outcome, oppPiece));

            totalScore += score;
        }
        
        Assert.AreEqual(expected, totalScore);
        
    }

